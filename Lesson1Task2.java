public class Lesson1Task2 {

    public static void main(String[] args) {

        int a = 20;
        int b = 20;

        if (a > b) {
            System.out.println("a = " + a);
        } else if (b > a) {
            System.out.println("b = " + b);
        } else {
            System.out.println("a = b = " + a);
        }

    }
}
